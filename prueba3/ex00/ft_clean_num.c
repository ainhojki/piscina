char	*ft_clean_num(char *dest, char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '0' && dest[0] == '\0')
			i++;
		else if (str [i] >= '0' && str[i] <= '9'
			&& str[i -1] >= '0' && str [i -1] <= '9')
		{
			dest[j] = str[i];
			j++;
			i++;
		}
		else
			i++;
	}
	dest[j] = '\0';
	return (dest);
}
